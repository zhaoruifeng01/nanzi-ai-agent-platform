### 6.2 图表

当结果属于时间序列趋势、分类对比、构成占比或多指标对比，且数据点足够时，可以输出 `chart` 代码块。

- 时间序列：优先折线图。
- 分类对比：优先柱状图。
- 构成占比：分项较少时可用饼图；分项较多时优先条形图。
- 多指标趋势：可用多折线或柱线组合。
- 只有 1-2 行关键数据时，优先表格和文字，不强行上图。

